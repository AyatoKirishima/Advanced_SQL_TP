#E211 : L’utilisateur ‘fernand’ peut-il retirer (supprimer) l’utilisateur ‘casimir’ du SGBD ? 

-- EH BIEN OUI !

-- Un utilisateur avec un droit "CREATE USER" peut bien entendu créer et supprimer ses utilisateurs mais aussi supprimer les autres utilisateurs !

-- L'attribution CREATE USER à un utilisateur autre qu'un admin doit être évitée